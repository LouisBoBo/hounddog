@ActivityScope
public class BindForeignPresenter extends BasePresenter<BindForeignContract.Model, BindForeignContract.View> {
    @Inject
    RxErrorHandler mErrorHandler;
    @Inject
    Application mApplication;
    @Inject
    ImageLoader mImageLoader;
    @Inject
    AppManager mAppManager;

    @Inject
    public BindForeignPresenter (BindForeignContract.Model model, BindForeignContract.View rootView) {
        super(model, rootView);
    }

    /**
     * 绑定手机号码
     * @param bean
     */
    public void submitBindMobile(ModifyPasswordPutBean bean, String sid, boolean isChangeBind){
        mModel.submitBindMobile(bean, sid)
                .subscribeOn(Schedulers.io())
                .doOnSubscribe(disposable -> {
                    mRootView.showLoading();
                }).subscribeOn(AndroidSchedulers.mainThread())
                .observeOn(AndroidSchedulers.mainThread())
                .doFinally(() -> {
                    mRootView.hideLoading();
                })
                .compose(RxLifecycleUtils.bindToLifecycle(mRootView))
                .subscribe(new ErrorHandleSubscriber<BaseBean>(mErrorHandler) {
                    @Override
                    public void onNext(BaseBean baseBean) {
                        mRootView.submitBindMobileSuccess(baseBean, isChangeBind);
                    }
                });
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mErrorHandler = null;
        this.mAppManager = null;
        this.mImageLoader = null;
        this.mApplication = null;
    }
}
