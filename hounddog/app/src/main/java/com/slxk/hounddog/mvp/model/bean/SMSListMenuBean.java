package com.slxk.hounddog.mvp.model.bean;

/**
 * 短信透传bean
 */
public class SMSListMenuBean {

    private int id; //
    private String name; // 名称
    private String content;//内容

    public SMSListMenuBean(int id, String name){
        super();
        this.id = id;
        this.name = name;
    }
    public SMSListMenuBean(int id, String name, String content){
        this.id = id;
        this.name = name;
        this.content = content;

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
