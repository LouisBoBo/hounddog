@ActivityScope
public class ConnectDevicePresenter extends BasePresenter<ConnectDeviceContract.Model, ConnectDeviceContract.View> {
    @Inject
    RxErrorHandler mErrorHandler;
    @Inject
    Application mApplication;
    @Inject
    ImageLoader mImageLoader;
    @Inject
    AppManager mAppManager;

    @Inject
    public ConnectDevicePresenter (ConnectDeviceContract.Model model, ConnectDeviceContract.View rootView) {
        super(model, rootView);
    }

    public void getBluetoothUpgradeVersion(String url, String device_number){
        mModel.getBluetoothUpgradeVersion(url)
                .subscribeOn(Schedulers.io())
                .doOnSubscribe(disposable -> {
                    mRootView.showLoading();
                }).subscribeOn(AndroidSchedulers.mainThread())
                .observeOn(AndroidSchedulers.mainThread())
                .doFinally(() -> {
                    mRootView.hideLoading();
                })
                .compose(RxLifecycleUtils.bindToLifecycle(mRootView))
                .subscribe(new ErrorHandleSubscriber<BluetoothUpgradeVersionBean>(mErrorHandler) {
                    @Override
                    public void onNext(BluetoothUpgradeVersionBean bluetoothUpgradeVersionBean) {
                        mRootView.getBluetoothUpgradeVersionSuccess(bluetoothUpgradeVersionBean, device_number);
                    }
                });
    }

    /**
     * 上报蓝牙升级结果
     * @param url
     */
    public void submitUpgradeResult(String url){
        mModel.submitUpgradeResult(url)
                .subscribeOn(Schedulers.io())
                .doOnSubscribe(disposable -> {
                    mRootView.showLoading();
                }).subscribeOn(AndroidSchedulers.mainThread())
                .observeOn(AndroidSchedulers.mainThread())
                .doFinally(() -> {
                    mRootView.hideLoading();
                })
                .compose(RxLifecycleUtils.bindToLifecycle(mRootView))
                .subscribe(new ErrorHandleSubscriber<BaseBean>(mErrorHandler) {
                    @Override
                    public void onNext(BaseBean baseBean) {

                    }
                });
    }

    @Override
    public void onDestroy() {
        super.onDestroy();
        this.mErrorHandler = null;
        this.mAppManager = null;
        this.mImageLoader = null;
        this.mApplication = null;
    }
}
